     public final void leerTrabajador(){
        try{
            File archivo = new File("src\\ficheros\\trabajador.txt");
            if(archivo.exists()){
                FileReader reader = new FileReader(archivo);
                BufferedReader br = new BufferedReader(reader);
                
                String linea;
                while((linea = br.readLine())!=null){
                    StringTokenizer st = new StringTokenizer(linea,";");
                    String cod = st.nextToken().trim();
                    String nom = st.nextToken().trim();
                    String ape = st.nextToken().trim();
                    String dni = st.nextToken().trim();
                    String tel = st.nextToken().trim();
                    String fec = st.nextToken().trim();
                    
                    
                    
                    Object[] obj = new Object[]{
                        cod, nom, ape, dni, tel, fec
                    };
                    modelo.addRow(obj);
                }
                //liberar recursos
                br.close();
            }
            else{
                JOptionPane.showMessageDialog(null, 
                        "No se encuentra la ruta del archivo");
            }
            
        }catch(IOException ex){
            JOptionPane.showMessageDialog(null,
                "Error Leer Fichero:\n"+ ex.getMessage());
        }
    }
            
        
