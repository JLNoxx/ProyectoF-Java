    private void btnTraRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTraRegistrarActionPerformed
SimpleDateFormat dFormat= new SimpleDateFormat("dd-MM-yyyy");
        try{
    FileWriter fw; //para escribir
    PrintWriter pw;//para impirimir
    
    //creando archivo (con ubicacion)
    fw = new FileWriter("src\\ficheros\\trabajador.txt",true);
    //imprimir archivo
    pw= new PrintWriter(fw);

    
    Trabajador obj = new Trabajador();

    obj.setTra_codigo(Integer.parseInt(txtTraCodigo.getText()));
    obj.setTra_nombre(txtTraNombre.getText());
    obj.setTra_apellido(txtTraApellido.getText());
    obj.setTra_dni(Integer.parseInt(txtTraDNI.getText()));
    obj.setTra_telefono(Integer.parseInt(txtTraTelefono.getText()));
    String date=dFormat.format(dtcTraFechaNac.getDate());
   
    
//eniviar datos al archivo

pw.println(obj.getTra_codigo()+";"+
        obj.getTra_nombre()+";"+
        obj.getTra_apellido()+";"+
        obj.getTra_dni()+";"+
        obj.getTra_telefono()+";"+
        date+";"
        
);
            //cerar la impresion
            pw.close();
            //enciar mensaje de exito
            JOptionPane.showMessageDialog(null,"Se registraron los datos con exito");

//eniviar daotos a la tabla
Object[] fila =new Object[6];

fila[0]=obj.getTra_codigo();
fila[1]=obj.getTra_nombre();
fila[2]=obj.getTra_apellido();
fila[3]=obj.getTra_dni();
fila[4]=obj.getTra_telefono();
fila[5]=date;
modelo.addRow(fila);



}catch(Exception ex) {//se atrapa los errores y si se soluciona
       //enviar mensaje de fracaso
       JOptionPane.showMessageDialog(null, 
               "Error Fichero: " + ex.getMessage());
       }   
//limpiar

txtTraCodigo.setText(null);
txtTraNombre.setText(null);
txtTraApellido.setText(null);
txtTraDNI.setText(null);
txtTraTelefono.setText(null);

txtTraCodigo.requestFocus();

      
    }//GEN-LAST:event_btnTraRegistrarActionPerformed

