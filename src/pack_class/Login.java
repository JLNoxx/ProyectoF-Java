package pack_class;
public class Login {
	String nickname;
        String contra;
        String nombre;
        String correo;
        String codigo;
        int telefono;
        int anexo;

    public Login() {
    }

    public Login(String nickname, String contra, String nombre, String correo, String codigo, int telefono, int anexo) {
        this.nickname = nickname;
        this.contra = contra;
        this.nombre = nombre;
        this.correo = correo;
        this.codigo = codigo;
        this.telefono = telefono;
        this.anexo = anexo;
    }
        
        
        
}